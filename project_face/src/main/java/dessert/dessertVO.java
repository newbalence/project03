package dessert;

public class dessertVO {
	 
	private int dessertNum;
 
	private String dessertName;

	private String dessertPay;

	private String dessertType;

	public int getDessertNum() {
		return dessertNum;
	}

	public void setDessertNum(int desserNum) {
		this.dessertNum = desserNum;
	}

	public String getDessertName() {
		return dessertName;
	}

	public void setDessertName(String dessertName) {
		this.dessertName = dessertName;
	}

	public String getDessertPay() {
		return dessertPay;
	}

	public void setDessertPay(String dessertPay) {
		this.dessertPay = dessertPay;
	}

	public String getDessertType() {
		return dessertType;
	}

	public void setDessertType(String dessertType) {
		this.dessertType = dessertType;
	}

	
}