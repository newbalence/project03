package burger;

public class burgerVO {
	
	/* 버거 번호 */ 
	private int burgerNum;


	/* 버거이름 */ 
	private String burgerName;


	/* 버거 가격 */ 
	private String burgerPay;


	public int getBurgerNum() {
		return burgerNum;
	}


	public void setBurgerNum(int burgerNum) {
		this.burgerNum = burgerNum;
	}


	public String getBurgerName() {
		return burgerName;
	}


	public void setBurgerName(String burgerName) {
		this.burgerName = burgerName;
	}


	public String getBurgerPay() {
		return burgerPay;
	}


	public void setBurgerPay(String burgerPay) {
		this.burgerPay = burgerPay;
	}


	


}
